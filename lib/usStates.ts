export const US_STATE_CODES: readonly string[] = US_STATES.map((s) => s.code)

const STATE_CODE_SET = new Set<string>(US_STATE_CODES)
const STATE_NAME_BY_CODE = new Map<string, string>(US_STATES.map((s) => [s.code, s.name]))

export function isUsStateCode(code: string | null | undefined): boolean {
  return typeof code === 'string' && STATE_CODE_SET.has(code)
}

export function stateName(code: string | null | undefined): string | null {
  if (!code) return null
  return STATE_NAME_BY_CODE.get(code) ?? null
}
